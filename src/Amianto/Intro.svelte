  <div class="mdc-typography--headline4 uppercase">COS'È L'AMIANTO E PERCHÉ VIENE SCAVATO?</div>
  <div class="mdc-typography--body1">
    Con il termine <span class="highlight">amianto</span> o asbesto viene identificato un minerale naturale a struttura fibrosa, conosciuto sin dall'antichitá
    per le sue proprietà termoisolanti e fonoassorbenti. Nel XX secolo, grazie alla sua
    <span class="highlight">economicità di produzione</span>, è stato largamente adottato da tutto il mondo in innumerevoli applicazioni industriali ed
    edilizie.
    <br /><br />
    Con il tempo però tale materiale si è rivelato <span class="highlight">nocivo</span> per la salute dell'uomo, a causa del rilascio di fibre che, se inalate,
    possono provocare patologie <span class="highlight">gravi ed irreversibili</span> all'apparato respiratorio, tra cui le più note sono asbestosi, carcinomi polmonari
    e mesoteliomi.
  </div>
</div>

