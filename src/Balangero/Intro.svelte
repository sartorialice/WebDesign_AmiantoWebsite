<div class="left">
  <div class="mdc-typography--headline4 uppercase">La storia della miniera</div>
  <div class="mdc-typography--body1">
    L’Amiantifera di Balangero, situata sul Monte San Vittore nella provincia di Torino, oltre ad essere stata per decenni la
    <span class="highlight">più grande</span>
    cava d'amianto dell'Occidente, è stata in primo luogo un'immensa fonte di lavoro e ricchezza per la comunità balangerese e i paesi limitrofi.
    <br /><br />
    Le prime attività di estrazione vennero avviate nel 1918, mentre l’impianto di macinazione e separazione dell’amianto entrò in funzione nel 1921. Con il passare
    degli anni vennero potenziate le opere di scavo e ingranditi gli impianti di lavorazione, tanto da raggiungere il
    <span class="highlight">picco di produzione</span>
    di 36000 tonnellate annue.
    <br /><br />
    In pieno boom di produzione, nel 1983 l'Amiantifera di Balangero S.p.A. venne ceduta, dalla Eternit e dalle Manifatture Colombo, ai fratelli Puccini di Roma,
    ma nel giro di 7 anni la società subì una grave involuzione e nel 1990 venne <span class="highlight">chiusa per fallimento</span> e i dipendenti licenziati.
  </div>
</div>

<div class="right">
  <div class="mdc-typography--headline4 uppercase primary">Quanta sofferenza ha causato?</div>
  <div class="mdc-typography--body1">
    Con la chiusura della miniera e le leggi sulla pericolosità dell’amianto, è stato avviato un progetto di messa in sicurezza del luogo a causa delle numerose
    <span class="highlight">vittime e malati</span> accertati tra gli ex lavoratori e le loro famiglie. Gli specialisti hanno esaminato le storie sanitarie di
    1.966 ex addetti all’Amiantifera. Su questo totale, emerge un risultato sconcertante: fra essi ci sono
    <span class="highlight">214 vittime</span> riferibili proprio al contatto con la fibra killer.
    <br /><br />
    Non solo, negli anni si sono susseguite una serie di inchieste a favore degli operai, indagini che si conclusero con delle
    <span class="highlight">condanne</span> per gli ex dirigenti della cava. Nel 2012 furono quaranta i casi sotto esame: venticinque morti e quindici malati, tra
    i quali anche casi di normali cittadini e non solo di lavoratori della cava.
  </div>
</div>

