<div class="mdc-typography--headline1 uppercase">Minaçu, Brasile</div>

<div class="left">
  <div class="mdc-typography--headline4 uppercase">Una storia controversa</div>
  <div class="mdc-typography--body1">
    Minaçu è un comune brasiliano dello stato del Goiás dove opera la <span class="highlight">SAMA</span>, un'industria franco-brasiliana dedita all’estrazione
    di amianto crisotilo, e dove è presente la più grande miniera di amianto del Brasile chiamata <span class="highlight">Cana Brava</span>. L'amianto ha reso
    Minaçu uno dei comuni più ricchi del paese.
    <br /><br />
    Le leggi federali in Brasile attestano chiaramente la pericolosità dell’amianto. Ciononostante, l’estrazione, la manipolazione e la commercializzazione di quest’ultimo
    sono rimaste attive fino al 2017, anno in cui la Corte Suprema Federale ha <span class="highlight">vietato</span> l’uso del minerale.
    <br /><br />
    Nel 2019, però, il governatore del Brasile ha emanato una <span class="highlight">legislazione</span> nello stato di Goiás, approvando l’estrazione e l'esportazione
    di amianto, facendo continuare così l’operato di Cana Brava. Questa mossa politica ha portato gli abitanti di Minaçu in una situazione delicata di fronte a un
    bivio: in qualsiasi momento una decisione del tribunale potrebbe interrompere l'attività mineraria della città, caduta proprio in un periodo economico difficile.
  </div>
</div>

<div class="right">
  <div class="mdc-typography--headline4 uppercase primary">L'opinione degli abitanti</div>
  <div class="mdc-typography--body1">
    Alcuni residenti desiderano che la città abbandoni l’amianto e si rinnovi, secondo altri invece senza amianto la città è finita.
    <span class="italic">"Se SAMA si ferma, la città si ferma"</span>, ha detto Joaquim de Souza, 54 anni, operaio della SAMA, che vive vicino alla massiccia
    <span class="highlight">collina di detriti</span>
    di amianto di Minaçu.
    <br /><br />
    Molti si riferiscono all'azienda come la <span class="italic">"<span class="highlight">madre</span> di Minaçu, che si prende cura dei suoi abitanti"</span>,
    mentre altri la ritengono in realtá una <span class="italic">"madre perversa che fa ammalare le persone, voltandogli poi le spalle"</span>.
    <br /><br />
    In tanti ancora negano che l'amianto sia <span class="highlight">dannoso</span>, sostenendo che
    <span class="italic">"la maggior parte dei lavoratori fumava, e solo dopo essersi ammalati per le sigarette hanno accusato SAMA"</span>.
  </div>
</div>

